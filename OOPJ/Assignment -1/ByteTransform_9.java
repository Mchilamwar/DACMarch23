class  ByteTransform_9{


    public static void main(String[] args) {
        
        // Byte b=64;
        // Byte b1=new Byte(b);

        Byte b1=64; // AutoBoxing

        System.out.println("Byte Value :"+b1.byteValue());// Return value as byte

        System.out.println("Int Value :"+b1.intValue());// Return value as int

        System.out.println("float Value :"+b1.floatValue());// float

        System.out.println("short Value :"+b1.shortValue());//short

        System.out.println("Double Value :"+b1.doubleValue());//double

        System.out.println("Long Value :"+b1.longValue());//long




    }


}